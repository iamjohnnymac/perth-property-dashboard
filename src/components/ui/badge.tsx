import * as React from "react"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const badgeVariants = cva(
  "inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default: "border-transparent bg-primary text-primary-foreground",
        secondary: "border-transparent bg-secondary text-secondary-foreground",
        destructive: "border-transparent bg-destructive text-destructive-foreground",
        outline: "text-foreground",
        house: "border-transparent bg-emerald-100 text-emerald-800",
        townhouse: "border-transparent bg-blue-100 text-blue-800",
        unit: "border-transparent bg-purple-100 text-purple-800",
        pool: "border-transparent bg-cyan-100 text-cyan-800",
        underOffer: "border-orange-300 bg-orange-50 text-orange-700",
        priceDrop: "border-transparent bg-red-100 text-red-800",
        beach: "border-transparent bg-sky-100 text-sky-800",
        motivated: "border-transparent bg-amber-100 text-amber-800",
        bestValue: "border-transparent bg-green-100 text-green-800",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
)

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return (
    <div className={cn(badgeVariants({ variant }), className)} {...props} />
  )
}

export { Badge, badgeVariants }